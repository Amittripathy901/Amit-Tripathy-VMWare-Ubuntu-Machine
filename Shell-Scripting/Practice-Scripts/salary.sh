Based on the revised income tax slabs for FY 2025–26 under the new tax regime, here’s a simplified calculation for a 60 LPA (₹60,00,000 per annum) salary. (This calculation assumes no additional deductions except the standard deduction of ₹75,000 and does not factor in other withholdings like PF or professional tax.)

1. **Determine Taxable Income:**  
 • Gross Salary = ₹60,00,000  
 • Standard Deduction = ₹75,000  
 • **Taxable Income = ₹60,00,000 – ₹75,000 = ₹59,25,000**

2. **Apply the New Tax Slabs:**  
 • **₹0–4,00,000:** 0% → Tax = ₹0  
 • **₹4,00,001–8,00,000:** 5% on ₹4,00,000 = ₹20,000  
 • **₹8,00,001–12,00,000:** 10% on ₹4,00,000 = ₹40,000  
 • **₹12,00,001–16,00,000:** 15% on ₹4,00,000 = ₹60,000  
 • **₹16,00,001–20,00,000:** 20% on ₹4,00,000 = ₹80,000  
 • **₹20,00,001–24,00,000:** 25% on ₹4,00,000 = ₹1,00,000  
 • **Above ₹24,00,000:** 30% on the remaining (₹59,25,000 – ₹24,00,000 = ₹35,25,000)  
   Tax = 30% of ₹35,25,000 = ₹10,57,500  

 • **Total Tax before Cess =**  
  ₹20,000 + ₹40,000 + ₹60,000 + ₹80,000 + ₹1,00,000 + ₹10,57,500 = **₹13,57,500**

3. **Add Health & Education Cess:**  
 • Cess at 4% on ₹13,57,500 = 0.04 × ₹13,57,500 = **₹54,300**

 • **Total Income Tax Liability = ₹13,57,500 + ₹54,300 = ₹14,11,800**

4. **Compute Net (In‐Hand) Salary:**  
 • Annual Take‐Home = Gross Salary – Income Tax  
  = ₹60,00,000 – ₹14,11,800 ≈ **₹45,88,200 per annum**  
 • Monthly Take‐Home ≈ ₹45,88,200 ÷ 12 ≈ **₹3,82,350 per month**

**Summary:**  
With a 60 LPA salary and under the new income tax slabs for FY 2025–26, you’d have an approximate annual in-hand salary of about **₹45.9 L** (roughly **₹3.82 L per month**) after income tax is deducted.

*Note:* This is a simplified calculation based solely on income tax; actual in-hand salary might be lower once other statutory deductions (like EPF, professional tax, etc.) are taken into account.

